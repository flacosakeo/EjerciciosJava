public class clase19for {
    public static void main(String[] args) {
        
        //variables
        int n = 5;
        int f = 1;

        for(int i=1; i<=n; i++){
            f=f*i;
        }
        System.out.println("El factorial de "+ n +" es "+ f);
        System.out.println("*** El programa ha finalizado correctamente ***");  
    }

}
