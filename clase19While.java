public class clase19While {
    public static void main(String[] args) {
        
        //inicializo la variable
        int contador = 0;

        //bucle while
        while(contador<=10){
            System.out.println("contador: "+contador);
            contador++;//es lo mismo que contador=contador+1
        }
        System.out.println("fin del programa");    
    }
    
}
